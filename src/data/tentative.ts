import type { TripEvent } from '../types'
import { generateId } from '../lib/utils'

const now = new Date().toISOString()

function plan(
  partial: Omit<TripEvent, 'id' | 'createdAt' | 'updatedAt' | 'sortOrder'> & { sortOrder?: number },
): TripEvent {
  return {
    id: generateId(),
    sortOrder: partial.sortOrder ?? 0,
    createdAt: now,
    updatedAt: now,
    ...partial,
  }
}

